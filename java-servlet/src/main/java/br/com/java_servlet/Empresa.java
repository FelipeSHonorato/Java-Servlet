package br.com.java_servlet;

public class Empresa {
	
	private String nome;
	private Integer id;
	
	public Empresa() {
		super();
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
	

}
